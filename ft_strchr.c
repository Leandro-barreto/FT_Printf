#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	if (!c)
		return (0);
	while (*s)
	{
		if (*s == c)
			return ((char *)s);
		s++;
	}
	if (*s == c)
		return ((char *)s);
	return (NULL);
}
